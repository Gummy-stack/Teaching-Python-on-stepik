"""
Две операции арифметики
Научимся складывать/вычитать два числа в зависимости от введенного знака. Сначала программа получает два
произвольных числа, затем одну из двух строк: "+" или "-". Произведите сложение или вычитание (в этом случае
нужно из первого числа вычесть второе) и выведите результат на экран.
"""
a = float(input())
b = float(input())
c = input()
if c == '+':
    print(a + b)
elif c == '-':
    print(a - b)
else:
    print('error')
